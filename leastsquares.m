x = [0; 1; 2; 3; 4; 5; 6; 7];
y = [4.0; 3.1; 2.8; 2.5; 2.3; 2.2; 2.1; 2.0];
ln_y = [log(4); log(3.1); log(2.8); log(2.5); log(2.3); log(2.2); log(2.1); log(2)];

plot(x, y, 'ro');
hold on;

A1 = [0 1;1 1;2 1; 3 1; 4 1; 5 1; 6 1; 7 1];
A2 = [1 0 0 0; 1 1 1 1; 1 2 4 8; 1 3 9 27; 1 4 16 64; 1 5 25 125; 1 6 36 216; 1 7 49 343];

x1 = inv(A1' * A1) * A1' * y;
x2 = inv(A2' * A2) * A2' * y;
x3 = inv(A1' * A1) * A1' * ln_y;

m1 = x1(1);
b1 = x1(2);

d2 = x2(1);
c2 = x2(2);
b2 = x2(3);
a2 = x2(4);

a3 = x3(1);
ln_c = x3(2);
c3 = exp(ln_c);

f = @(x) m1*x + b1;
g = @(x) a2*x^3 + b2*x^2 + c2*x + d2;
h = @(x) c3*exp(a3*x);

fplot(f);
fplot(g);
fplot(h);
hold off;